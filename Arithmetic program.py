# Python Calculation

Operator = input(("Enter the operator(+ - * /) :"))
num1 = int(input("Enter the first number :"))
num2 = int(input("Enter the second number :"))

if Operator == "+":
    result= num1 + num2
    print(result)

elif Operator == "-":
    result = num1 - num2
    print(result)

elif Operator == "*":
    result = num1 * num2
    print(result)

elif Operator == "/":
  result = num1 / num2
  print(result)